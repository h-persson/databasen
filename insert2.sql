    e.employee_id,
    d::date AS salary_date,
    (
        CASE
            WHEN e.employee_id <= 5  THEN 80000   
            WHEN e.employee_id <= 15 THEN 70000   
            WHEN e.employee_id <= 30 THEN 60000   
            WHEN e.employee_id <= 40 THEN 50000   
            ELSE                     40000         
        END
    )
    + e.employee_id * 150          
    + EXTRACT(MONTH FROM d) * 50   
    AS salary
FROM employee e
CROSS JOIN generate_series('2025-01-01'::date, '2025-12-01'::date, interval '1 month') AS d;

